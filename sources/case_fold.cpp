uint32_t case_fold(uint32_t rune) noexcept
{
    auto iter = std::lower_bound(
        std::begin(fold_data),
        std::end(fold_data),
        rune_pair(rune, rune),
        [](const rune_pair& a, const rune_pair& b) { return a.first < b.first; });
    if (iter != std::end(fold_data) && iter->first == rune)
        return iter->second;
    return rune;
}

std::string case_fold(StringRef str)
{
    std::string result;
    result.reserve(str.size());
    auto begin = str.begin(), end = str.end();
    while (begin != end)
    {
        uint32_t rune = utf8::next(begin, end);
        utf8::append(case_fold(rune), std::back_inserter(result));
    }
    return result;
}
